def main():
    parser = argparse.ArgumentParser(description='NES → SMS ROM Packer & Scaffold Generator')
    sub = parser.add_subparsers(dest='cmd')

    p_gen = sub.add_parser('generate')
    p_gen.add_argument('--manifest', required=True)
    p_gen.add_argument('--assets', default=None)
    p_gen.add_argument('--backend', default='wla-dx', choices=['wla-dx', 'sdcc'])
    p_gen.add_argument('--mapper', default='sega')
    p_gen.add_argument('--out', required=True)

    p_stubs = sub.add_parser('gen-stubs')
    p_stubs.add_argument('--symbols', required=True)
    p_stubs.add_argument('--out', required=True)

    p_ph = sub.add_parser('patch-header')
    p_ph.add_argument('--rom', required=True)
    p_ph.add_argument('--region', default='6')
    p_ph.add_argument('--out', required=True)

    p_mf = sub.add_parser('write-makefile')
    p_mf.add_argument('--out', required=True)

    p_test = sub.add_parser('test')
    p_test.add_argument('--rom', required=True)
    p_test.add_argument('--emulator', default='emulicious')
    p_test.add_argument('--report', default='validation/test_results.txt')

    args = parser.parse_args()

    if args.cmd == 'generate':
        cmd_generate(args)
    elif args.cmd == 'gen-stubs':
        symbols = json.loads(Path(args.symbols).read_text())
        if isinstance(symbols, dict):
            symbols = list(symbols.values())
        gen_stubs_from_symbols(symbols, Path(args.out))
    elif args.cmd == 'patch-header':
        cmd_patch_header(args)
    elif args.cmd == 'write-makefile':
        out = Path(args.out)
        out.mkdir(parents=True, exist_ok=True)
        (out / 'Makefile').write_text(MAKEFILE_CONTENT, encoding='utf-8')
        write_linker_script(out)
        print(f"[write-makefile] Makefile + link.sms written to {out}/")
    elif args.cmd == 'test':
        cmd_test(args)
    else:
        parser.print_help()


if __name__ == '__main__':
    main()
