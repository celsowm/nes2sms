def gen_stubs_from_symbols(symbols: list, out_dir: Path):
    lines = [
        '; Generated stubs — one per NES subroutine in symbols.json',
        '; Replace each stub body with ported Z80 code.',
        '; Reference: SKILL.md § Step 5',
        '',
        '.section "GameStubs" FREE',
        '',
    ]
    for sym in symbols:
        name = sym.get('name', 'Unknown')
        addr = sym.get('cpu_addr', '?')
        bank = sym.get('bank', '?')
        comment = sym.get('comment', '')
        lines += [
            f'; {"="*60}',
            f'; NES Label: {name}',
            f'; NES CPU addr: {addr} | Bank: {bank}',
            f'; {comment}' if comment else '; (no comment in symbols.json)',
            f'; TODO: port this routine to Z80',
            f'; {"="*60}',
            f'NES_{name}:',
            f'    ; TODO',
            f'    ret',
            '',
        ]
    lines.append('.ends')
    (out_dir / 'game_stubs.asm').write_text('\n'.join(lines), encoding='utf-8')
    print(f"[gen-stubs] Wrote {len(symbols)} stubs to {out_dir}/game_stubs.asm")


# ---------------------------------------------------------------------------
# SMS Header Patcher
# ---------------------------------------------------------------------------

def cmd_patch_header(args):
    data = bytearray(Path(args.rom).read_bytes())
    region = REGION_CODES.get(args.region, 6) if isinstance(args.region, str) else int(args.region)
    patched = patch_sms_header(data, region)
    out_path = Path(args.out)
    out_path.write_bytes(patched)
    cs = compute_checksum(bytes(patched))
    print(f"[patch-header] Wrote {out_path} | checksum=${cs:04X} | region={region}")


# ---------------------------------------------------------------------------
# Project Scaffold Generator
# ---------------------------------------------------------------------------

def cmd_generate(args):
    out = Path(args.out)
    (out / 'hal').mkdir(parents=True, exist_ok=True)
    (out / 'stubs').mkdir(parents=True, exist_ok=True)
    (out / 'assets').mkdir(parents=True, exist_ok=True)

    manifest = json.loads(Path(args.manifest).read_text())
    prg_size = manifest.get('nes_header', {}).get('prg_size', 32768)
    rom_banks = max(8, (prg_size // 16384) + 4)  # +4 for init + vectors + assets banks

    # Copy assets if provided
    if args.assets:
        assets_src = Path(args.assets)
        for f in assets_src.iterdir():
            if f.is_file():
                import shutil
                shutil.copy(f, out / 'assets' / f.name)

    # Write all source files
    (out / 'main.asm').write_text(MAIN_ASM, encoding='utf-8')
    (out / 'memory.inc').write_text(MEMORY_INC.format(banks=rom_banks), encoding='utf-8')
    (out / 'init.asm').write_text(INIT_ASM, encoding='utf-8')
    (out / 'interrupts.asm').write_text(INTERRUPTS_ASM, encoding='utf-8')
    (out / 'assets.asm').write_text(ASSETS_ASM, encoding='utf-8')
    (out / 'hal' / 'vdp.asm').write_text(HAL_VDP_ASM, encoding='utf-8')
    (out / 'hal' / 'psg.asm').write_text(HAL_PSG_ASM, encoding='utf-8')
    (out / 'hal' / 'input.asm').write_text(HAL_INPUT_ASM, encoding='utf-8')
    (out / 'hal' / 'mapper.asm').write_text(HAL_MAPPER_ASM, encoding='utf-8')
    (out / 'stubs' / 'game_logic.asm').write_text(GAME_LOGIC_STUB_ASM, encoding='utf-8')
    write_linker_script(out, rom_banks)
    (out / 'Makefile').write_text(MAKEFILE_CONTENT, encoding='utf-8')

    print(f"[generate] SMS project scaffold written to {out}/")
    print(f"[generate] Backend: {args.backend} | ROM banks: {rom_banks}")
    print(f"[generate] Next: cd {out} && make")


