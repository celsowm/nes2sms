# ---------------------------------------------------------------------------
# SMS Header Constants
# ---------------------------------------------------------------------------

SMS_HEADER_MAGIC = b'TMR SEGA'
SMS_HEADER_OFFSET_32K = 0x7FF0

REGION_CODES = {
    'japan':    4,
    'export':   5,   # GG overseas
    'sms-us':   6,
    'sms-eu':   7,
    'sms-jp':   5,
    'gg-jp':    6,
}

ROM_SIZE_CODES = {
    8192:    0xA,
    16384:   0xB,
    32768:   0xC,
    49152:   0xD,
    65536:   0xE,
    131072:  0xF,
}


def compute_checksum(data: bytes, header_offset: int = SMS_HEADER_OFFSET_32K) -> int:
    total = 0
    for i, b in enumerate(data):
        if header_offset <= i <= header_offset + 15:
            continue
        total += b
    return total & 0xFFFF


def patch_sms_header(data: bytearray, region: int = 6) -> bytearray:
    size_code = 0xC  # default 32KB
    for size, code in ROM_SIZE_CODES.items():
        if len(data) >= size:
            size_code = code

    offset = SMS_HEADER_OFFSET_32K
    if len(data) < offset + 16:
        data.extend(b'\x00' * (offset + 16 - len(data)))

    # Write magic
    data[offset:offset + 8] = SMS_HEADER_MAGIC
    data[offset + 8] = 0x00
    data[offset + 9] = 0x00

    # Compute and write checksum
    checksum = compute_checksum(bytes(data), offset)
    struct.pack_into('<H', data, offset + 10, checksum)

    # Product code (homebrew: all zeros)
    data[offset + 12] = 0x00
    data[offset + 13] = 0x00
    data[offset + 14] = 0x00

    # Region + size
    data[offset + 15] = ((region & 0xF) << 4) | (size_code & 0xF)
    return data


